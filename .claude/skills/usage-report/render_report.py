def _build_cloud_installs_table(
    metrics: dict,
    prev_metrics: dict,
) -> str:
    """Render the lifetime cloud-installs comparison table.

    Returns a markdown table string identical to what analyze_telemetry's
    tables-*.md produces but built straight from per_cloud_unique_installs.
    """
    current = metrics.get("per_cloud_unique_installs", {})
    previous = prev_metrics.get("per_cloud_unique_installs", {}) if prev_metrics else {}

    last_seen_per_cloud = _last_seen_per_cloud_from_metrics(metrics)

    lines = [
        "| Cloud Provider | Previous | Current | Change | Last Startup | Last Heartbeat |",
        "|----------------|---------:|--------:|-------:|--------------|----------------|",
    ]
    for cloud in sorted(current.keys()):
        cur = current.get(cloud, 0)
        prev = previous.get(cloud, 0) if prev_metrics else 0
        delta = _format_combined_delta(cur, prev)
        last_startup = last_seen_per_cloud.get(cloud, {}).get("last_startup", "n/a")
        last_heartbeat = last_seen_per_cloud.get(cloud, {}).get("last_heartbeat", "n/a")
        lines.append(
            f"| {cloud} | {prev} | {cur} | {delta} | {last_startup} | {last_heartbeat} |"
        )
    return "\n".join(lines)


def _last_seen_per_cloud_from_metrics(
    metrics: dict,
) -> dict[str, dict[str, str]]:
    """Walk identified_instances and compute last startup/heartbeat date per cloud."""
    out: dict[str, dict[str, str]] = {}
    for inst in metrics.get("identified_instances", []):
        cloud = inst.get("cloud") or "unknown"
        latest = inst.get("latest_seen", "")[:10]
        first = inst.get("first_seen", "")[:10]
        if cloud not in out:
            out[cloud] = {"last_startup": first, "last_heartbeat": latest}
        else:
            if first > out[cloud]["last_startup"]:
                out[cloud]["last_startup"] = first
            if latest > out[cloud]["last_heartbeat"]:
                out[cloud]["last_heartbeat"] = latest
    return out


def _build_engagement_table(
    active_csv_rows: list[dict],
    n_recent: int = 3,
) -> str:
    """Render the recent-days DAI table."""
    if not active_csv_rows:
        return "| (no data) | | | | | | |"
    last_n = active_csv_rows[-n_recent:]
    lines = []
    for row in last_n:
        # Column names from generate_active_instances_chart.py:
        # date, daily_active, ma7, streak7, cumulative_installs,
        # dai_pct_of_total, likely_alive_7d, dai_pct_of_likely_alive
        lines.append(
            f"| {row['date']} | {row['daily_active']} | {row['ma7']} | "
            f"{row['streak7']} | {row['cumulative_installs']} | "
            f"{row['dai_pct_of_total']}% | {row['dai_pct_of_likely_alive']}% |"
        )
    return "\n".join(lines)


