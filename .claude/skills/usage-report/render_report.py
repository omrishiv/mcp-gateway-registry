def _format_delta_int(
    delta: int,
) -> str:
    """Format an integer delta with sign and a +N / -N / 0 string."""
    if delta > 0:
        return f"+{delta:,}"
    if delta < 0:
        return f"{delta:,}"
    return "0"


def _format_delta_pct(
    current: int | float,
    previous: int | float,
) -> str:
    """Compute % change and format with sign."""
    if previous == 0:
        return ""
    pct = (current - previous) / previous * 100
    if abs(pct) < 0.5:
        return "(0%)"
    sign = "+" if pct >= 0 else ""
    return f"({sign}{pct:.0f}%)"


def _format_combined_delta(
    current: int | float,
    previous: int | float,
) -> str:
    """Render '+N (+X%)' for a delta + percent change."""
    delta = current - previous
    delta_str = _format_delta_int(int(delta)) if isinstance(current, int) else f"{delta:+.2f}"
    pct_str = _format_delta_pct(current, previous)
    if pct_str:
        return f"{delta_str} {pct_str}".strip()
    return delta_str


def _short_id(
    full_id: str,
    length: int = 12,
) -> str:
    """Truncate a UUID to its first N chars + ellipsis."""
    if not full_id:
        return ""
    return f"{full_id[:length]}..."


