DATE_RE = re.compile(r"^\d{4}-\d{2}-\d{2}$")


def _previous_date(
    date_str: str,
) -> str:
    """Return YYYY-MM-DD for date - 1."""
    d = datetime.strptime(date_str, "%Y-%m-%d")
    return (d - timedelta(days=1)).strftime("%Y-%m-%d")


def _find_previous_metrics(
    search_dir: str,
    current_date: str,
) -> str | None:
    """Find the most recent metrics-*.json file before current_date."""
    candidates: list[tuple[str, str]] = []
    for entry in os.listdir(search_dir):
        full = os.path.join(search_dir, entry)
        if not os.path.isdir(full) or not DATE_RE.match(entry):
            continue
        if entry >= current_date:
            continue
        candidate = os.path.join(full, f"metrics-{entry}.json")
        if os.path.exists(candidate):
            candidates.append((entry, candidate))
    if not candidates:
        return None
    candidates.sort(reverse=True)
    return candidates[0][1]


