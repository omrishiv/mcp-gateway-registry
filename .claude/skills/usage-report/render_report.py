def _build_sticky_cloud_compute_table(
    metrics: dict,
    prev_metrics: dict,
) -> str:
    """Render the sticky breakdown by cloud + compute."""
    current = metrics.get("sticky_cloud_compute", {})
    previous = prev_metrics.get("sticky_cloud_compute", {}) if prev_metrics else {}

    if not current:
        return "| (no data) | | | | |"

    total = sum(current.values()) or 1
    lines = [
        "| Cloud | Compute | Instances | % | Change vs Previous |",
        "|-------|---------|----------:|---|-------------------:|",
    ]
    # Sort by count descending
    for key, count in sorted(current.items(), key=lambda x: -x[1]):
        cloud, compute = key.split("/", 1)
        prev_count = previous.get(key, 0)
        delta = count - prev_count
        delta_str = _format_delta_int(delta) if delta != 0 else "0"
        pct = count / total * 100
        lines.append(f"| {cloud} | {compute} | {count} | {pct:.0f}% | {delta_str} |")
    return "\n".join(lines)


def _read_section_from_tables_md(
    path: str,
    section_header: str,
) -> str:
    """Extract a markdown section (between `## section_header` and the next `## `)."""
    if not os.path.exists(path):
        return f"(tables file not found at {path})"
    with open(path) as f:
        content = f.read()

    pattern = re.compile(
        rf"##\s+{re.escape(section_header)}.*?\n(.*?)(?=\n##\s+|\Z)",
        re.DOTALL,
    )
    match = pattern.search(content)
    if not match:
        return f"(section '{section_header}' not found in tables file)"
    return match.group(1).strip()


def _build_detection_by_version_narrative(
    csv_path: str,
) -> str:
    """Render a data-driven narrative about cloud_detection_method by version.

    Reads the detection-by-version CSV (which has columns: version, env, dmi,
    ecs_meta, k8s_heuristic, imds, unknown, (field absent), total). Picks the
    two newest non-dev release versions and the two oldest (still in top N),
    and builds a comparison sentence based on actual green/red/grey shares.
    """
    if not os.path.exists(csv_path):
        return "_Detection-by-version data not available for narrative._"

    with open(csv_path) as f:
        rows = list(csv.DictReader(f))

    if not rows:
        return "_Detection-by-version data is empty._"

    GREEN = ("env", "dmi", "ecs_meta", "k8s_heuristic", "imds")
    RED = "unknown"
    GREY = "(field absent)"

    def _share(row: dict, methods: tuple[str, ...] | str) -> float:
        total = int(row.get("total", 0)) or 1
        if isinstance(methods, str):
            return int(row.get(methods, 0)) / total * 100
        return sum(int(row.get(m, 0)) for m in methods) / total * 100

    # Identify "release" versions (skip the "other" rollup, dev branches, sha-only refs)
    is_release_re = re.compile(r"^v?\d+\.\d+\.\d+(?:-p\d+)?$")
    releases = [r for r in rows if is_release_re.match(r.get("version", "")) and int(r.get("total", 0)) >= 5]

    if not releases:
        return "_No release-version rows with sufficient sample size for narrative._"

    # Sort releases descending: newest version strings last
    def _version_key(v: str) -> tuple:
        v = v.lstrip("v").split("-")[0]
        parts = v.split(".")
        return tuple(int(p) if p.isdigit() else 0 for p in parts)

    releases.sort(key=lambda r: _version_key(r["version"]))
    newest = releases[-2:]
    oldest = releases[:2]

    parts = []
    for row in reversed(newest):
        green_pct = _share(row, GREEN)
        red_pct = _share(row, RED)
        v = row["version"]
        n = row["total"]
        parts.append(
            f"`{v}` (n={n}): {green_pct:.0f}% successful detection (env, dmi, ecs_meta, k8s_heuristic, imds), "
            f"{red_pct:.0f}% unknown."
        )

    if oldest and oldest[0]["version"] != newest[-1]["version"]:
        oldest_release = oldest[0]
        red_pct = _share(oldest_release, RED)
        grey_pct = _share(oldest_release, GREY)
        v = oldest_release["version"]
        n = oldest_release["total"]
        parts.append(
            f"For comparison, the oldest tracked release `{v}` (n={n}) has "
            f"{red_pct:.0f}% unknown and {grey_pct:.0f}% field-absent. The "
            f"unknown share is dominated by the EKS+Helm gap that #1093 / PR #1106 "
            f"closed in 1.24.2; field-absent represents pre-1.23.0 instances that "
            f"never emit `cloud_detection_method`."
        )
    return " ".join(parts)


def _build_architecture_patterns(
    metrics: dict,
    liveness: dict,
) -> str:
    """Generate architecture-pattern bullets from data.

    Looks at:
      - longest-running customer profile
      - top Most-Active profile (search-heavy)
      - top Largest-Catalog profile (catalog-heavy)
      - top upgrade-trajectory profile (release-tracking)
      - smallest cloud cohort with sticky representation (GCP/Azure surprising survivor)
    """
    bullets = []

    instances = metrics.get("identified_instances", [])
    internal_ids = set(metrics.get("internal_instance_ids", []))
    customers = [
        i for i in instances
        if (i.get("registry_id_full") or i.get("registry_id", "")) not in internal_ids
    ]
    # Index by both truncated and full IDs since stickiness.longest_non_internal_id
    # is stored as the truncated form
    by_id = {}
    for i in customers:
        full_id = i.get("registry_id_full", "")
        short_id = i.get("registry_id", "")
        if full_id:
            by_id[full_id] = i
        if short_id:
            by_id[short_id] = i

    # 1. Longest-running profile
    stickiness = metrics.get("stickiness", {})
    longest_id = stickiness.get("longest_non_internal_id", "")
    longest_days = stickiness.get("longest_non_internal_days", 0)
    if longest_id and longest_id in by_id:
        inst = by_id[longest_id]
        bullets.append(
            f"**{inst['cloud'].upper()} / {inst['compute']} / {inst['auth']} long-running stack.** "
            f"`{longest_id}` is the longest-running non-internal instance at "
            f"{longest_days} days. Customer-story candidate."
        )

    # 2. Most active (highest activity score = max_servers + max_agents + max_skills + total_search_queries)
    def _activity_score(i):
        return (
            i.get("max_servers", 0)
            + i.get("max_agents", 0)
            + i.get("max_skills", 0)
            + i.get("total_search_queries", 0)
        )
    if customers:
        most_active = max(customers, key=_activity_score)
        bullets.append(
            f"**{most_active['cloud'].upper()} / {most_active['compute']} / {most_active['auth']} most-active stack.** "
            f"`{most_active.get('registry_id', '')[:12]}...` (version `{most_active.get('version', '?')}`) "
            f"leads the Most Active table with {most_active.get('total_search_queries', 0):,} lifetime search queries."
        )

    # 3. Largest catalog
    def _catalog_score(i):
        return i.get("max_servers", 0) + i.get("max_agents", 0) + i.get("max_skills", 0)
    if customers:
        largest = max(customers, key=_catalog_score)
        score = _catalog_score(largest)
        if score > 0:
            bullets.append(
                f"**{largest['cloud'].upper()} / {largest['compute']} / {largest['auth']} catalog-heavy stack.** "
                f"`{largest.get('registry_id', '')[:12]}...` leads with {score:,} "
                f"registered objects (servers + agents + skills)."
            )

    # 4. Most engaged operator (longest upgrade chain)
    trajectories = metrics.get("upgrade_trajectories", [])
    if trajectories:
        top_traj = trajectories[0]
        # Try full id first, fall back to short id (both are indexed in by_id)
        rid_full = top_traj.get("registry_id_full", "")
        rid_short = top_traj.get("registry_id", "")
        inst = by_id.get(rid_full) or by_id.get(rid_short)
        if inst:
            bullets.append(
                f"**{inst['cloud'].upper()} / {inst['compute']} / {inst['auth']} upgrade-ladder stack.** "
                f"`{rid_short}` has tracked "
                f"{top_traj.get('version_changes', 0)} version changes, the deepest upgrade chain in the fleet."
            )

    # 5. Surprising survivor: longest-running customer NOT on AWS
    non_aws = [c for c in customers if c.get("cloud") not in ("aws", "unknown")]
    if non_aws:
        longest_non_aws = max(non_aws, key=lambda i: i.get("events", 0))
        bullets.append(
            f"**{longest_non_aws['cloud'].upper()} / {longest_non_aws['compute']} / {longest_non_aws['auth']} surprising survivor.** "
            f"`{longest_non_aws.get('registry_id', '')[:12]}...` represents the largest non-AWS cohort. "
            f"Disproportionately sticky for the smallest cloud segments."
        )

    return "\n".join(f"{i + 1}. {b}" for i, b in enumerate(bullets))


def _read_compute_snapshots(
    path: str,
    top_n: int = 10,
) -> str:
    """Read the per-platform-snapshots markdown file and return its table.

    Trims to the top N most recent snapshots after the bolded current row.
    """
    if not os.path.exists(path):
        return "(compute-platform-snapshots file not found)"
    with open(path) as f:
        content = f.read()
    # The file is just a table; return it as-is, trimmed
    lines = content.splitlines()
    if len(lines) <= top_n + 2:
        return content
    # Keep header (2 lines) + bolded current row + top N other rows
    return "\n".join(lines[: top_n + 3])


def _build_template_vars(
    args: argparse.Namespace,
) -> dict:
    """Assemble the full dict of template substitution values."""
    date_str = args.date
    output_dir = Path(args.output_dir)
    search_dir = args.search_dir or str(output_dir.parent)

    # Load all the structured outputs
    metrics_path = output_dir / f"metrics-{date_str}.json"
    liveness_path = output_dir / f"liveness-{date_str}.json"
    ltv_path = output_dir / f"ltv-spend-{date_str}.json"
    forecast_path = output_dir / f"install-forecast-{date_str}.json"
    github_path = output_dir / "github_stats.json"
    contributors_path = output_dir / "github_contributors_count.txt"
    active_csv_path = output_dir / f"active-instances-{date_str}.csv"
    lifetime_buckets_csv_path = output_dir / f"lifetime-buckets-{date_str}.csv"
    compute_snapshots_path = output_dir / f"compute-platform-snapshots-{date_str}.md"
    tables_md_path = output_dir / f"tables-{date_str}.md"

    metrics = _read_json(str(metrics_path))
    liveness = _read_json(str(liveness_path))
    ltv = _read_json(str(ltv_path))
    forecast = _read_json(str(forecast_path))
    github = _read_json(str(github_path))

    # Previous report
    prev_metrics_path = _find_previous_metrics(search_dir, date_str)
    prev_metrics = _read_json(prev_metrics_path) if prev_metrics_path else {}
    prev_liveness_path = _find_previous_liveness(search_dir, date_str)
    prev_liveness = _read_json(prev_liveness_path) if prev_liveness_path else {}
    prev_github_path = _find_previous_github_stats(search_dir, date_str)
    prev_github = _read_json(prev_github_path) if prev_github_path else {}

    prev_date = ""
    if prev_metrics_path:
        prev_date = prev_metrics_path.split("/metrics-")[1].replace(".json", "")

    # Contributors
    github_contributors = 0
    if contributors_path.exists():
        github_contributors = int(contributors_path.read_text().strip())

    # Engagement CSV
    active_rows = _read_active_instances_csv(str(active_csv_path))

    # Lifetime buckets CSV (for retention-over-time table)
    lifetime_rows = _read_lifetime_buckets_csv(str(lifetime_buckets_csv_path))

    # ===== Compute all the variables =====

