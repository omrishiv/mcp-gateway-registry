"""Render the usage-report markdown deterministically from analyzer outputs.

This module replaces LLM-driven report generation with a pure-data pipeline:

  metrics-*.json  +  liveness-*.json  +  ltv-spend-*.json  +
  install-forecast-*.json  +  github_stats.json  +  active-instances-*.csv  +
  lifetime-buckets-*.csv  +  compute-platform-snapshots-*.md  +  tables-*.md
            |
            v
  report_template.md  ->  rendered ai-registry-usage-report-YYYY-MM-DD.md

Every cell value comes from JSON or CSV files. The Recommendations section
is generated by rule-based triggers (see recommendations.py). The LLM is
not in the report-content path.

Usage:
    /usr/bin/python3 .claude/skills/usage-report/render_report.py \\
        --date YYYY-MM-DD \\
        --output-dir .scratchpad/usage-reports/YYYY-MM-DD/ \\
        --search-dir .scratchpad/usage-reports/

Reads from:
  $output-dir/metrics-{date}.json
  $output-dir/liveness-{date}.json
  $output-dir/ltv-spend-{date}.json
  $output-dir/install-forecast-{date}.json
  $output-dir/github_stats.json
  $output-dir/active-instances-{date}.csv
  $output-dir/lifetime-buckets-{date}.csv
  $output-dir/compute-platform-snapshots-{date}.md
  $output-dir/tables-{date}.md  (for the few sub-tables we reuse verbatim)

  Plus the previous report's metrics-*.json (auto-discovered under search-dir)
  for delta calculations.
"""

